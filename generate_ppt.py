def add_slide(title_text, subtitle=None):
    """Add a blank slide with dark navy header bar"""
    slide = prs.slides.add_slide(prs.slide_layouts[6])  # blank
    # Header bar
    shape = slide.shapes.add_shape(MSO_SHAPE.RECTANGLE, 0, 0, prs.slide_width, Inches(1.1))
    shape.fill.solid()
    shape.fill.fore_color.rgb = DARK_NAVY
    shape.line.fill.background()
    # Title
    tf = shape.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.text = title_text
    p.font.size = Pt(28)
    p.font.bold = True
    p.font.color.rgb = WHITE
    p.alignment = PP_ALIGN.LEFT
    tf.margin_left = Inches(0.6)
    tf.margin_top = Inches(0.15)
    if subtitle:
        p2 = tf.add_paragraph()
        p2.text = subtitle
        p2.font.size = Pt(13)
        p2.font.color.rgb = RGBColor(0x97, 0xCB, 0xFF)
        p2.alignment = PP_ALIGN.LEFT
    # Green accent line
    line = slide.shapes.add_shape(MSO_SHAPE.RECTANGLE, 0, Inches(1.1), prs.slide_width, Inches(0.04))
    line.fill.solid()
    line.fill.fore_color.rgb = GREEN
    line.line.fill.background()
    # Footer
    foot = slide.shapes.add_textbox(Inches(0.6), Inches(7.0), Inches(12), Inches(0.4))
    ftf = foot.text_frame
    fp = ftf.paragraphs[0]
    fp.text = "工业称重业务 | 宏观经济与政策报告 | 2026年6月 | CONFIDENTIAL"
    fp.font.size = Pt(8)
    fp.font.color.rgb = GRAY
    return slide

def add_textbox(slide, left, top, width, height, text_lines, font_size=11, bold_first=True, color=DARK_TEXT):
    """Add a text box with multiple lines"""
    txBox = slide.shapes.add_textbox(Inches(left), Inches(top), Inches(width), Inches(height))
    tf = txBox.text_frame
    tf.word_wrap = True
    for i, line in enumerate(text_lines):
        if i == 0:
            p = tf.paragraphs[0]
        else:
            p = tf.add_paragraph()
        p.text = line
        p.font.size = Pt(font_size)
        p.font.color.rgb = color
        if bold_first and i == 0:
            p.font.bold = True
        p.space_after = Pt(2)
    return txBox

def add_kpi_box(slide, left, top, value, label, val_color=DARK_BLUE):
    """Add a KPI metric box"""
    shape = slide.shapes.add_shape(MSO_SHAPE.ROUNDED_RECTANGLE, Inches(left), Inches(top), Inches(1.8), Inches(1.3))
    shape.fill.solid()
    shape.fill.fore_color.rgb = LIGHT_BG
    shape.line.color.rgb = RGBColor(0xE5, 0xED, 0xE5)
    shape.line.width = Pt(0.5)
    tf = shape.text_frame
    tf.word_wrap = True
    tf.margin_left = Inches(0.1)
    tf.margin_right = Inches(0.1)
    p = tf.paragraphs[0]
    p.text = value
    p.font.size = Pt(24)
    p.font.bold = True
    p.font.color.rgb = val_color
    p.alignment = PP_ALIGN.CENTER
    p2 = tf.add_paragraph()
    p2.text = label
    p2.font.size = Pt(9)
    p2.font.color.rgb = GRAY
    p2.alignment = PP_ALIGN.CENTER

def add_table(slide, left, top, width, height, headers, rows, col_widths=None):
    """Add a styled table"""
    n_rows = len(rows) + 1
    n_cols = len(headers)
    tbl_shape = slide.shapes.add_table(n_rows, n_cols, Inches(left), Inches(top), Inches(width), Inches(height))
    tbl = tbl_shape.table
    if col_widths:
        for i, w in enumerate(col_widths):
            tbl.columns[i].width = Inches(w)
    # Header
    for i, h in enumerate(headers):
        cell = tbl.cell(0, i)
        cell.text = h
        cell.fill.solid()
        cell.fill.fore_color.rgb = DARK_NAVY
        for p in cell.text_frame.paragraphs:
            p.font.size = Pt(8)
            p.font.bold = True
            p.font.color.rgb = WHITE
    # Data rows
    for r, row in enumerate(rows):
        for c, val in enumerate(row):
            cell = tbl.cell(r+1, c)
            cell.text = str(val)
            if r % 2 == 0:
                cell.fill.solid()
                cell.fill.fore_color.rgb = RGBColor(0xFA, 0xFC, 0xFA)
            for p in cell.text_frame.paragraphs:
                p.font.size = Pt(7.5)
                p.font.color.rgb = DARK_TEXT
    return tbl

